    void center(JInternalFrame jif) {
        MainjDesktopPane1.add(jif);
        Dimension centersize = MainjDesktopPane1.getSize();
        Dimension jinternalframeSize = jif.getSize();
        int width = (centersize.width - jinternalframeSize.width) / 2;
        int height = (centersize.height - jinternalframeSize.height) / 2;
        jif.setLocation(width, height);
        jif.setVisible(true);
    }
    
