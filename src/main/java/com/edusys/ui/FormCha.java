    void startDongHo() {
        class TimeClock extends Thread {

            @Override
            public void run() {
                while (true) {
                    lblDongHo.setText(new SimpleDateFormat("hh:mm:ss a").format(Calendar.getInstance().getTime()));
                }
            }
        }
        TimeClock timeClock = new TimeClock();
        timeClock.start();
    }
